package ArraysEMatrizesMultidimensionais;

public class ExemploArray {
    public static void main(String[] args) {
        int[] array = {10, 20, 30, 40, 50}; // Declarando e inicializando o array

        for (int i = 0; i < array.length; i++) {
            System.out.println("Acessando posicao " +i+ ": " + array[i]); // Acessando elementos pelo indice.
        }
    }
}
